#include <windows.h>
#include <cstring>
#include <memory>
#include <stdexcept>

class ShellcodeExecutor {
public:
    explicit ShellcodeExecutor(const unsigned char* code, size_t size)
        : m_size(size) {
        // Allocate executable memory
        m_exec_mem = VirtualAlloc(nullptr, m_size, MEM_COMMIT | MEM_RESERVE, PAGE_EXECUTE_READWRITE);
        if (!m_exec_mem) {
            throw std::runtime_error("Failed to allocate executable memory");
        }

        // Copy shellcode into memory
        std::memcpy(m_exec_mem, code, m_size);
    }

    ~ShellcodeExecutor() {
        if (m_exec_mem) {
            VirtualFree(m_exec_mem, 0, MEM_RELEASE);
        }
    }

    void execute() const {
        // Cast to function pointer and execute
        reinterpret_cast<void(*)()>(m_exec_mem)();
    }

    // Delete copy operations for safety
    ShellcodeExecutor(const ShellcodeExecutor&) = delete;
    ShellcodeExecutor& operator=(const ShellcodeExecutor&) = delete;

private:
    void* m_exec_mem = nullptr;
    size_t m_size = 0;
};

